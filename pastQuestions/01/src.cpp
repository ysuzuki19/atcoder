#include <bits/stdc++.h>
using namespace std;

int main(){
  // start process
  int a, b;
  cin >> a >> b;
  if(a%2==0){
    if(b%2==0){
      cout << "Even" << endl;
    }else{
      cout << "Even" << endl;
    }
  }else{
    if(b%2==0){
      cout << "Even" << endl;
    }else{
      cout << "Odd" << endl;
    }
  }


  // end process
  return 0;
}
